using System;
using System.Collections.Generic;
using System.Text;

namespace Extract.Drawing
{
    /// <summary>
    /// Specifies a set of anchor points.
    /// </summary>
    public enum AnchorAlignment
    {
        /// <summary>
        /// The left-bottom point.
        /// </summary>
        LeftBottom,

        /// <summary>
        /// The center-bottom point.
        /// </summary>
        Bottom,

        /// <summary>
        /// The bottom-right point.
        /// </summary>
        RightBottom,

        /// <summary>
        /// The left-center point.
        /// </summary>
        Left,

        /// <summary>
        /// The center point.
        /// </summary>
        Center,

        /// <summary>
        /// The right-center point.
        /// </summary>
        Right,

        /// <summary>
        /// The left-top point.
        /// </summary>
        LeftTop,

        /// <summary>
        /// The center-top point.
        /// </summary>
        Top,

        /// <summary>
        /// The right top point.
        /// </summary>
        RightTop
    }
}
